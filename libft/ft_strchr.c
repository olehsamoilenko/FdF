char	*ft_strchr(const char *s, int c)
{
	int		i;
	char	*a;

	a = (char*)s;
	i = 0;
	if (c == 256)
		return ("");
	while (a[i])
	{
		if (a[i] == (char)c)
			return (&a[i]);
		i++;
	}
	if (c == 0)
		return (&a[i]);
	return (0);
}
